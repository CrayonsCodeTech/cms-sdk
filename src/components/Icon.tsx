export interface IconProps extends LucideProps {
  /** The name of the icon (from the ICON_MAP keys) */
  name: string;
  /** Fallback icon name if the provided name is not found */
  fallback?: string;
}

/**
 * Dynamic Icon component that renders Lucide icons by name.
 * Useful for CMS-driven icons where names are stored as strings.
 */
export function Icon({ name, fallback = "HelpCircle", ...props }: IconProps) {
  const IconComponent = ICON_MAP[name] ?? ICON_MAP[fallback] ?? HelpCircle;
  return React.createElement(IconComponent, props);
}
